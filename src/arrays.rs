use super::*;

macro_rules! impl_array {
    ($($n: expr),*) => {
        $(
            impl<'a, A: Arbitrary<'a>> Arbitrary<'a> for [A; $n] {
                valuetree!();
                type Strategy = [A::Strategy; $n];
                fn arbitrary() -> Self::Strategy {
                    any::<[A; $n]>()
                }
            }
        )*
    };
}

